#Bitwise (~) Not or One's Complement
# sign ပြောင်းချင်တဲ့အချိန်မှာသုံးတယ်။
# ဉပမာ - အပေါင်း ကနေ အနှုတ် ပြောင်း၊ အနှုတ်ကနေအပေါင်းပြောင်း
# a = ~10 ---> a = -(10+1) = -11

a = ~11#-(11+1)
print(a)

